% pfstools - matlab interface
%
% This is an entry page for pfstools matlab documentation.
%
% -----------------------------------------------------------------
% The most frequently used functions:
%
% PFS_READ_IMAGE - read image from file
% PFS_WRITE_IMAGE - write image to file
% PFSVIEW - view matrices as images using pfsview
%
% -----------------------------------------------------------------
% More specialized functions:
%
% PFS_TRANSFORM_COLORSPACE - transform between color spaces
% PFS_READ_LUMINANCE - read luminance image from file
% PFS_READ_RGB - read RGB image from file
% PFS_READ_XYZ - read XYZ image from file
% PFS_WRITE_LUMINANCE - write luminance image to file
% PFS_WRITE_RGB - write RGB image to file
% PFS_WRITE_XYZ - write XYZ image to file
% PFS_TEST_SHELL - diagnoze for possible problems if pfstools interface is not working
%
% -----------------------------------------------------------------
% Low-level interface, probably you do not need this:
%
% PFSOPEN - open pfs-stream for reading/writing
% PFSPOPEN - execute shell command and acquire its input / output stream
% PFSCLOSE - close pfs-stream
% PFSPCLOSE - close stream created with PFSPOPEN
% PFSGET - read one frame from pfs-stream
% PFSPUT - write one frame to pfs-stream
% PFS_SHELL - wrapper for calling pfstools shell programs from matlab
%
% -----------------------------------------------------------------
